package org.hibernate.orm.test.boot.models.xml.complete;

/**
 * @author Steve Ebersole
 */
public class Root {
	private Integer id;
	private String name;

	protected Root() {
		// for Hibernate use
	}

	public Root(Integer id, String name) {
		this.id = id;
		this.name = name;
	}

	public Integer getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
